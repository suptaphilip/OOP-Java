public class SwitchExample {
	public static void main(String[] args) {
		int day = 1;
		switch(day){
		case 6:
			System.out.println("Friday");
			break;	
		case 7:
			System.out.println("Saturday");
			break;	
		default:
				System.out.println("Looking for Sunday!");
			
		}
			
	}

}
